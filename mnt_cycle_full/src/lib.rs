fn modulus_bytes<F: PrimeField>() -> Vec<u8> {
    F::MODULUS.to_bytes_le()
}

fn pairing_digest<P: Pairing>(value: ark_ec::pairing::PairingOutput<P>) -> String {
    let mut bytes = Vec::new();
    value.serialize_compressed(&mut bytes).expect("pairing output serialization must succeed");
    to_hex(&bytes)
}

fn to_hex(bytes: &[u8]) -> String {
    let mut out = String::with_capacity(2 + bytes.len() * 2);
    out.push_str("0x");
    for b in bytes {
        write!(&mut out, "{b:02x}").unwrap();
    }
    out
}

fn short_digest(value: &str) -> String {
    if value.len() <= 34 {
        value.to_string()
    } else {
        format!("{}...{}", &value[..18], &value[value.len() - 12..])
    }
}

fn write_side_row(out: &mut String, r: &RelationSideReport, tower: &str) {
    writeln!(
        out,
        "| {:?} | {} | {} | {} | {} | {} | {} | {} | {} |",
        r.side,
        tower,
        r.miller_rounds,
        r.addition_steps,
        r.one_miller_transition_constraints,
        r.miller_transition_constraints,
        r.line_cache_relation_constraints,
        r.fe_residue_constraints,
        r.prepared_relation_constraints
    )
    .unwrap();
}
